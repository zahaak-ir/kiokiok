import hashlib

input_file_path = "V2/Ss.txt"
output_file_path_1 = "SsL.txt"
# output_file_path_2 = "SsF.txt"

completed_lines_hash = set()

input_file = open(input_file_path, "r")
output_file_1 = open(output_file_path_1, "w")
# output_file_2 = open(output_file_path_2, "w")

for line in input_file:
    hashValue = hashlib.md5(line.rstrip().encode('utf-8')).hexdigest()
    if hashValue not in completed_lines_hash:
        output_file_1.write(line)
        # output_file_2.write(line)
        completed_lines_hash.add(hashValue)

output_file_1.close()
# output_file_2.close()
input_file.close()

